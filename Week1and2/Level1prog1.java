//Program to calculate Harry's age in 2024
 public class Level1prog1
{
    public static void main(String args[])
    {
        //variable storing Harry's birth year
        int birth_year=2000;
        //variable storing the current year
        int current_year=2024;
        //calculating the age in 2024
        int age=current_year - birth_year;
        //displaying the age
        System.out.println("Harry's current age in 2024 is: "+age);
    }

    
}